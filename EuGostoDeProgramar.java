public class EuGostoDeProgramar {
    public static void main(String args[]){
        int i =0;
        while(i<20){
            System.out.println("Eu gosto de programar 'while'");
            i++;
        }
        
        i=0;
        
        do{
            System.out.println("Eu gosto de programar 'do while'");
            i++;
        }while(i<20);
        
        for(i=0;i<20;i++){
            System.out.println("Eu gosto de programar 'for'");
            
        }
    }
}
